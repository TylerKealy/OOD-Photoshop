package model;

/**
 * Stores the RGB value of a pixel.
 */
public class RGB {
  public int r;
  public int g;
  public int b;

  /**
   * Create an object to store RGB values.
   * @param r red
   * @param g green
   * @param b blue
   */
  public RGB(int r, int g, int b) {
    this.r = r;
    this.g = g;
    this.b = b;
  }
}
